int		ft_iterative_factorial(int nb)
{
	int	res;

	if (nb < 0)
		return (0);
	else if (nb == 1 || nb == 0)
		return (1);
	else
	{
		res = 1;
		while (nb > 1)
		{
			res *= nb;
			nb--;
		}
		return (res);
	}
}
