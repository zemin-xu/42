void		ft_putchar(char c);

void		rush01(int x, int y)
{
	int		a;
	int		b;

	a = 1;
	b = 1;
	while (b <= y)
	{
		while (a <= x)
		{
			if (a == 1 && b == 1)
				ft_putchar('/');
			else if ((a == 1 && b == y) || (a == x && b == 1))
				ft_putchar('\\');
			else if (a == x && b == y)
				ft_putchar('/');
			else
				ft_putchar((b == 1 || b == y || a == 1 || a == x) ? '*' : ' ');
			a++;
		}
		ft_putchar('\n');
		b++;
		a = 1;
	}
}
