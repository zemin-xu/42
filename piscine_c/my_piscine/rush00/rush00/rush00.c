void		ft_putchar(char c);

void		rush00(int x, int y)
{
	int		a;
	int		b;

	a = 1;
	b = 1;
	while (b <= y)
	{
		while (a <= x)
		{
			if (a == 1 || a == x)
				ft_putchar((b == 1 || b == y) ? 'o' : '|');
			else
				ft_putchar((b == 1 || b == y) ? '-' : ' ');
			a++;
		}
		ft_putchar('\n');
		b++;
		a = 1;
	}
}
