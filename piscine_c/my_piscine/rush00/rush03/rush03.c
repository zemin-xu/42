void		ft_putchar(char c);

void		rush03(int x, int y)
{
	int		a;
	int		b;

	a = 1;
	b = 1;
	while (b <= y)
	{
		while (a <= x)
		{
			if (a == 1)
				ft_putchar((b == 1 || b == y) ? 'A' : 'B');
			else if (a == x)
				ft_putchar((b == 1 || b == y) ? 'C' : 'B');
			else
				ft_putchar((b == 1 || b == y) ? 'B' : ' ');
			a++;
		}
		ft_putchar('\n');
		b++;
		a = 1;
	}
}
