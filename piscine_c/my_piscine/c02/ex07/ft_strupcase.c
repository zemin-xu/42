char		*ft_strupcase(char *str)
{
	int		i;
	int		space;

	space = 'a' - 'A';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] <= 'z' && str[i] >= 'a')
			str[i] -= space;
		i++;
	}
	return (str);
}
