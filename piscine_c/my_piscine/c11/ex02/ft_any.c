int			ft_any(char **tab, int (*f)(char *))
{
	int		i;
	int		flag;

	flag = 0;
	i = 0;
	while (tab[i])
	{
		if ((*f)(tab[i]) != 0)
			flag++;
		i++;
	}
	if (flag > 0)
		return (1);
	else
		return (0);
}
